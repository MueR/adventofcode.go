package main

import (
	"testing"
)

var example = `..F7.
.FJ|.
SJ.L7
|F--J
LJ...`

func TestPart1(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  int
	}{
		{
			name:  "Example",
			input: example,
			want:  8,
		},
		{
			name:  "Actual",
			input: input,
			want:  7145,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := part1(tt.input); got != tt.want {
				t.Errorf("part1() = %v, want %v", got, tt.want)
			}
		})
	}
}

