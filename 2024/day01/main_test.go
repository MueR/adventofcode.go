package main

import (
	"testing"
)

var example = `3   4
4   3
2   5
1   3
3   9
3   3
`

func TestPart1(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  int
	}{
		{
			name:  "Example",
			input: example,
			want:  11,
		},
		{
			name:  "Actual",
			input: input,
			want:  1197984,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := part1(tt.input); got != tt.want {
				t.Errorf("part1() = %v, want %v", got, tt.want)
			}
		})
	}
}

